    public void dynamicSearch(String value){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        workerTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(value));
    }
    
