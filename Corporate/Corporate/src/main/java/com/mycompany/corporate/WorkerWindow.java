    private void workerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_workerTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = workerTable.getSelectedRow();
        String id = model.getValueAt(selectedRow,0).toString();
        nameText.setText(model.getValueAt(selectedRow,1).toString());
        surnameText.setText(model.getValueAt(selectedRow,2).toString());
        fieldText.setText(model.getValueAt(selectedRow,3).toString());
        wageText.setText(model.getValueAt(selectedRow,4).toString());
    }//GEN-LAST:event_workerTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = workerTable.getSelectedRow();
        int id = (int)model.getValueAt(selectedRow,0);
        String name = nameText.getText();
        String surname = surnameText.getText();
        String field = fieldText.getText();
        String wage = wageText.getText();
        op.updateWorker(id,name,surname,field,wage);
        nameText.setText("");
        surnameText.setText("");
        fieldText.setText("");
        wageText.setText("");
        showWorkers();
    }//GEN-LAST:event_updateButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = workerTable.getSelectedRow();
        int id = (int)model.getValueAt(selectedRow,0);
        op.deleteWorker(id);
        nameText.setText("");
        surnameText.setText("");
        fieldText.setText("");
        wageText.setText("");
        showWorkers();
    }//GEN-LAST:event_removeButtonActionPerformed

    /**
     * @param args the command line arguments
     */
    
    public void showWorkers(){
        model.setRowCount(0);
        ArrayList<Worker> workers = new ArrayList<Worker>();
        workers = op.getWorkers();
        for(Worker worker : workers){
            Object[] temp = {worker.getId(),worker.getName(),worker.getSurname(),worker.getField(),worker.getWage()};
            model.addRow(temp);
        }
    }
    
